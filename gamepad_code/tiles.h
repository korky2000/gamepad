void drawLevel (int thisLevel) {
  for (int lvlY = 0; lvlY < tileH; lvlY++) {
    for (int lvlX = 0; lvlX < tileW; lvlX++) {
      int index = lvlX + (lvlY * tileW);
      int whatTile = levels[thisLevel][index];
      int finalX = lvlX * tileSize;
      int finalY = lvlY * tileSize;
      tft.drawRGBBitmap(finalX, finalY, blox2_PIX[whatTile], tileSize, tileSize);

    }
  }
}
